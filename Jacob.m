function a = Jacob(U0,U1)
global p q r h1 N h2
 

for k=1:N+1
    
    A(k,k) = p/h1^2 -3*q/h1^4 + (1/h2)+r*(2*U0(k)^2 +3*U1(k)^2 + 2*U0(k)*U1(k))/(4*h1^2);
    
    
        
    if (k>2 && k<N)    
        A(k,k-1) = 2*q/h1^4 -p/(2*h1^2)-r*(U0(k-1)^2 + 3*U1(k-1)^2 + 2*U0(k-1)*U1(k-1) )/(4*h1^2);

        A(k,k-2) = -q/(2*h1^4);

        A(k,k+1) = 2*q/h1^4 -p/(2*h1^2)-r*(U0(k+1)^2 + 3*U1(k+1)^2 + 2*U0(k+1)*U1(k+1) )/(4*h1^2);

        A(k,k+2) = -q/(2*h1^4);
    end

end

A(1,2) = 4*q/(h1^4)-p/(h1^2)-r*(U0(2)^2)/(2*h1^2)-r*(6*U1(2)^2+4*U0(2)*U1(2))/(4*h1^2);
A(1,3) = -q/(h1^4);
A(2,1) = 2*q/h1^4 -p/(2*h1^2)-r*(U0(1)^2 + 3*U1(1)^2 + 2*U0(1)*U1(1))/(4*h1^2);
A(N,N+1) = 2*q/h1^4 -p/(2*h1^2)-r*(U0(N+1)^2 + 3*U1(N+1)^2 + 2*U0(N+1)*U1(N+1))/(4*h1^2);
A(N+1,N-1) = -q/(h1^4);
A(N+1,N) = 4*q/(h1^4)-p/(h1^2)-r*(U0(N)^2)/(2*h1^2)-r*(6*U1(N)^2+4*U0(N)*U1(N))/(4*h1^2);
    
    
a = A;
%A
end